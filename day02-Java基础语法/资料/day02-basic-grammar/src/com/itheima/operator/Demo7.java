package com.itheima.operator;

public class Demo7 {
    public static void main(String[] args) {
        // 目标：掌握关系运算符
        int a = 10;
        int b = 5;
        System.out.println(a == b); // false
        System.out.println(a != b); // true
        System.out.println(a > b);  // true
        System.out.println(a >= b); // true
        System.out.println(a < b);  // false
        System.out.println(a <= b); // false
    }
}
